# ---------- Table 8: Top-N candidates -------------------------------------

def build_table_8(out_dir: Path, top_n: int = 20) -> Optional[Path]:
    p = Path("artifacts/predictions/top_candidates.csv")
    if not p.exists():
        logger.warning(f"Table 8 skipped — missing {p}; run run_full_repurposing_pipeline.py")
        return None
    with p.open(encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
    if not rows:
        return None

    rows = rows[:top_n]
    lines = _table_header(
        "top_candidates",
        f"Top-{top_n} ranked drug repurposing candidates by final fused evidence score.",
        "r l l c c c c", source=str(p),
    )
    lines.append(r"Rank & Compound & Disease & Score & Tier & KG (RotatE) & QSVC \\")
    lines.append(r"\midrule")
    for i, r in enumerate(rows, 1):
        lines.append(
            f"{i} & {_esc(r.get('compound', '—'))} & {_esc(r.get('disease', '—'))} & "
            f"{float(r.get('final_score', 0)):.4f} & {r.get('confidence_tier', '—')} & "
            f"{float(r.get('kg_rotate_score', 0)):.3f} & "
            f"{float(r.get('qsvc_score', 0)):.3f} \\\\"
        )
    lines.extend(_table_footer())

    out_path = out_dir / "table8_top_candidates.tex"
    _write_tex(lines, out_path)
    return out_path


