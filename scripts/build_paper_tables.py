# ---------- Driver --------------------------------------------------------

_BUILDERS = {
    1: build_table_1,
    4: build_table_4,
    5: build_table_5,
    6: build_table_6,
    7: build_table_7,
    8: build_table_8,
    9: build_table_9,
}


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--all", action="store_true",
                        help="Build every table (skips ones with missing inputs)")
    parser.add_argument("--table", type=int, default=None,
                        help="Build a single table by number")
    parser.add_argument("--tables", type=str, default=None,
                        help="Comma-separated list, e.g. 1,4,8")
    parser.add_argument("--output", default="docs/tables",
                        help="Output directory for .tex files (default docs/tables)")
    args = parser.parse_args()

    if args.table is None and not args.tables and not args.all:
        parser.error("Specify --all, --table N, or --tables 1,4,8")

    out_dir = Path(args.output)
    targets: List[int]
    if args.all:
        targets = sorted(_BUILDERS.keys())
    elif args.tables:
        targets = [int(t) for t in args.tables.split(",")]
    else:
        targets = [args.table]

    built: List[Path] = []
    skipped: List[int] = []
    for t in targets:
        if t not in _BUILDERS:
            logger.warning(f"Table {t} not yet supported")
            skipped.append(t)
            continue
        result = _BUILDERS[t](out_dir)
        if result is None:
            skipped.append(t)
        else:
            built.append(result)

    logger.info(f"=== {len(built)} table(s) built, {len(skipped)} skipped ===")
    return 0


if __name__ == "__main__":
    sys.exit(main() or 0)
